        f"‚è≥ –ù–∞—á–∏–Ω–∞—é –æ–±—Ä–∞–±–æ—Ç–∫—É\n–í—Å–µ–≥–æ —Å—Ç—Ä–æ–∫: {total}\n–û–±—Ä–∞–±–æ—Ç–∫–∞..."
